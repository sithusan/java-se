package language_fundamentals;

public class WritingArrays {
    public static void main(String[] args) {

        int array[] = { 1, 2, 3, 4, 5 };
        String strArray[] = { "Hello", "Java" };

        int[][] mutlti = { { 1, 2, 3 }, { 4, 5, 6 }, { 7, 8, 9 } };

        System.out.println(array[0]);
        System.out.println(strArray[0]);
        System.out.println(mutlti[0][0]);
    }
}
